// 问题生成提示词
// ============================================

/** 生成推荐问题的提示词模板 */
export function buildGenerateQuestionsPrompt(referenceQuestions: string[]): string {
  return `你是一个创意问题生成器。请参考以下问题的风格和主题，生成6个新的、有启发性、是用户可以问 AI 的问题。

参考问题风格：
${referenceQuestions.map((q, i) => `${i + 1}. ${q}`).join("\n")}

要求：
1. 保持类似的风格和语气
2. 问题要简短精炼
3. 每个问题独立成行
4. 不要编号，直接输出问题内容
5. 生成6个问题
6. 问题长度短一些

请直接输出6个问题，每行一个：`;
}

/** 追问生成的上下文提示词模板 */
export function buildFollowUpContextPrompt(context: string): string {
  return `以下是对话内容：

${context}

请基于此生成三个追问问题。`;
}

